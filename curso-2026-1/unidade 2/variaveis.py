# verificação de tipos de dados
"""
Este código serve para
verificar quais os tipos de dados de
cada variável!
"""
idade = 32 # numerico (inteiro)
print(idade)
print(type(idade))
nota = 6.7 # numerico (real)
print(nota)
print(type(nota))
nome = "Mauricio" # literal (String)
print(nome)
print(type(nome))
estaVago = False # lógico (booleano)
print(estaVago)
print(type(estaVago))
